CREATE OR ALTER view [dbo].[AN_PRIEM] AS
SELECT
PRIEM._Number,
AN_E.ФИО_СОТРУДНИКА,
DOLZHNOST._Description AS [ДОЛЖНОСТЬ],
FIZ_LIC._IDRRef AS IDRREF_FIZ

FROM _Document214 AS PRIEM

LEFT JOIN _Document214_VT4353 AS PRIEM_SOTR ON PRIEM_SOTR._Document214_IDRRef = PRIEM._IDRRef
LEFT JOIN AN_EMPLOYEES AS AN_E ON AN_E._IDRRef = PRIEM_SOTR._Fld4355RRef
LEFT JOIN _Reference51 AS DOLZHNOST ON DOLZHNOST._IDRRef = PRIEM_SOTR._Fld4358RRef
LEFT JOIN _Reference135 AS FIZ_LIC ON FIZ_LIC._IDRRef = PRIEM_SOTR._Fld4356RRef
GO


